var $menuButton = $('#responsive-menu-button');
var $buttonSubmit = $('#button-submit')
var $contentPanel = $('.content-panel');
var $body = $('body');

var mobileMenu = function() {

// function to toggle slide menu when hamburger icon is clicked
    $menuButton.click(function() {
        
        $buttonSubmit.addClass('mobile');
        
        $('.mobile').click(function() {
            slideIn();
        });

        if ($(this).hasClass('in')) {
            slideOut();

        }else if ($(this).hasClass('out')) {
            slideIn();
        };
        
        function slideOut() {
            $contentPanel.animate({
                  left: "0px"
                }, 200);

                $body.animate({
                  left: "70%"
                }, 200);
            $menuButton.addClass('out').removeClass('in');
        };
        
        function slideIn() {
            $contentPanel.animate({
                    left: "-70%"
                }, 200);

                $body.animate({
                    left: "0px"
                }, 200);
            $buttonSubmit.removeClass('mobile')
            $menuButton.addClass('in').removeClass('out');
        };
    });  
};

$(document).ready(mobileMenu);



